package models;

public class BasicInfoModel {

    private String faNumber;
    private String accountName;
    private String faReference;

    public String getFaNumber() {
        return faNumber;
    }
    public void setFaNumber(String faNumber) {
        this.faNumber = faNumber;
    }

    public String getAccountName() {
        return accountName;
    }
    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public String getFaReference() {
        return faReference;
    }
    public void setFaReference(String faReference) {
        this.faReference = faReference;
    }
}
